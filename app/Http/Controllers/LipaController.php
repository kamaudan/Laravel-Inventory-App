     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       $lipa = new Lipa;
       $lipa->TillsCreated=$request->input('till');
       $lipa->TillNo=$request->input('month');
       $lipa->Month=$request->input('tillno');
       $lipa->branch=$request->input('branch');
       $lipa->save();

       Session::flash('success', 'The details have been saved');

       return redirect('/lipa/create')->with('Success', 'Details stored');
   }
