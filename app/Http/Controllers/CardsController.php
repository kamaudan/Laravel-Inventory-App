     */
    public function store(Request $request)
    {
        $card = new Card;
        $card->Invoice=$request->input('invoice');
        $card->Balances=$request->input('balances');
        $card->branch =$request->input('branch');
        $card->size= $request->input('size');
        $card->serial= $request->input('serial');
        $card->price= $request->input('price');
        $card->cash=$request->input('cash');
        $card->save();

        Session::flash('success', 'The details have been saved');

        return redirect('/card/create');
    }

